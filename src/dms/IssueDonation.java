    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        
        int subjectid=Integer.valueOf(SubjectID.getText());
        int amount =Integer.valueOf(Amount.getText());
        int projectid=Integer.valueOf(ProjectID.getText());
        if(Total != 0){
            Donated d=new Donated(projectid,subjectid,amount);
            Donatedlist dlist=new Donatedlist();
            dlist.Insert(d);
            dlist.comit();
            Funddistributionlist flist=new Funddistributionlist();
            Funddistribution f=new Funddistribution(projectid,subjectid,1);
            flist.Insert(f);
            flist.updatestatus();
            RemainingBalance.setText(String.valueOf(Total-amount));
            Total=Total-amount;
            SubjectID.setText("");
            Amount.setText("");
        }
        else{
            Output.setText("Cannot Issue Donation");
        }
        

    }//GEN-LAST:event_AddActionPerformed

