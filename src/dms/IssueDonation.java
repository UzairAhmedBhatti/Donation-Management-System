    private void EnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterActionPerformed
        // TODO add your handling code here:
        String name=ProjectName.getText();
        Projectlist plist=new Projectlist();
        plist.getdata();
        Project p =new Project();
        p=plist.Searchbyname(name);
        if(p!=null){
            Funddistributionlist flist=new Funddistributionlist();
            flist.getdata();
            for(int i=0;i<flist.FD.size();i++){
                if(flist.FD.get(i).getProjectID()==p.getProjectID() && flist.FD.get(i).getDone()==0){
                    Object[] row = new Object[2];
                    DefaultTableModel model= (DefaultTableModel)ShortListedTable.getModel();
                    row[0]=flist.FD.get(i).getProjectID();
                    row[1]=flist.FD.get(i).getSubjectID();
                    model.addRow(row);
                }
            }
        }
    }//GEN-LAST:event_EnterActionPerformed

